package poo;

public class Patinete {
	public String nome;
	public String material;
	public String cor;
	public double velocidadeMaxima;
	public int peso;
	
	public Patinete(String nome, String material, String cor, double velocidadeMaxima, int peso) {
		super();
		this.nome = nome;
		this.material = material;
		this.cor = cor;
		this.velocidadeMaxima = velocidadeMaxima;
		this.peso = peso;
	}
	

}
