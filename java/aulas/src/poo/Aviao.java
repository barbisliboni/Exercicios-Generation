package poo;

public class Aviao {
	
	public String modelo;
	public double altura;
	public double peso;
	public int maxPessoas;
	public double velocidadeMaxima;
	
	public Aviao(String modelo, double altura, double peso, int maxPessoas, double velocidadeMaxima) {
		super();
		this.modelo = modelo;
		this.altura = altura;
		this.peso = peso;
		this.maxPessoas = maxPessoas;
		this.velocidadeMaxima = velocidadeMaxima;
	}
}
